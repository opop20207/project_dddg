package com.dddg.project_dddg;

public class TeamData {
    public String team;
    public String top;
    public String jgl;
    public String mid;
    public String bot;
    public String sup;

    public  TeamData(){
    }

    public TeamData(String team, String top, String jgl, String mid, String bot, String sup) {
        this.team = team;
        this.top = top;
        this.jgl = jgl;
        this.mid = mid;
        this.bot = bot;
        this.sup = sup;
    }
}
